金額の大小にかかわらず、請け負った建設工事には主任技術者が必要です。
「下請だから関係ない」ということはなく、むしろ現場の品質・安全を実際に担うのは下請です。

> 補足：発注者から直接請け負った元請（特定建設業者）が、下請に出す金額の合計が大きい場合は、
> 元請側に**監理技術者**を置く義務が生じます。下請側に求められるのは主任技術者です。

