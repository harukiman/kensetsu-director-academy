技術者は通常、複数の現場を担当できますが、**重要な工事（公共性のある施設や多数の人が利用する施設の工事）で請負金額が一定以上**の場合は、その現場に**専任（その現場に専ら従事し、他の現場と兼任しない）**でなければなりません。

